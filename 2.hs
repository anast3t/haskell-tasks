sqroot a b c | ((a == 0) && (b == 0)) = error "Корней нет" 
             | ((a == 0) && (b /= 0)) = -b/c
             -- | (d == 0) = error "Один корень"
             -- | (d < 0) = error "Нет действительных корней"
             | (a/=0) = (x1,x2) --error "Оба корня есть"
                where 
                 x1 = ((-b) + sqd) / (2*a)
                 x2 = ((-b) - sqd) / (2*a) 
                 d = (b * b) - (4 * a * c)
                 sqd = sqrt (d)